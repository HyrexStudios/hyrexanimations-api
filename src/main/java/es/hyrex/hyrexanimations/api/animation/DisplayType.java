package es.hyrex.hyrexanimations.api.animation;

/**
 * The <b>DisplayType</b> enum is used to specify the type of display of the animation.
 */
public enum DisplayType {
    /**
     * The animation will be displayed in the title.
     */
    TITLE,
    /**
     * The animation will be displayed in the subtitle.
     */
    SUBTITLE,
    /**
     * The animation will be displayed in the action bar.
     */
    ACTIONBAR,
}
